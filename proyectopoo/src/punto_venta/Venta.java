    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        // Conversión de variable
        String nombre_buscar = txtBusqueda.getText();
        lblNombre.setText("No existe");
        
        // Foreach para que recorra todos los productos
        for (Producto p : Registro.productos) {
            // Verifica que el nombre a buscar este contenido dentro del nombre
            if (p.getNombre().contains(nombre_buscar)) {
                // Se saca los datos en los siguientes campos
                lblNombre.setText(p.getNombre());
                lblStock.setText(String.valueOf(p.getCantidad()));
                lblPrecio1.setText(String.valueOf(p.getPrecio()));

            } else {
                // Condicional en caso de que no encuentre ninguno de los 
                // productos registrados
                if (lblNombre.getText() == "No existe") {
                    limpiar();
                }
            }
        }


    }//GEN-LAST:event_buscarActionPerformed

