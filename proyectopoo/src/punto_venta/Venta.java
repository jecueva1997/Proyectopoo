    double iva = 0;
    double total = 0;
    int stock = 0;
    int cont = 0;
    String nombres = "";
    // Lista para guardar los nombres
    private String[] prodNombres = new String[100];
    // Es un formateador para que me presente solo 2 números en la presentación 
    // de pantalla 
    private java.text.DecimalFormat formato = new java.text.DecimalFormat("0.00"); 
    // Botón que sirve para la compra del producto
    private void comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprarActionPerformed
        // Variables
        String mensaje = "";
        double precio;
        // Conversión de variable para luego hacer el cálculo del subtotal
        precio = Double.parseDouble(lblPrecio1.getText());
        // Conversión de variable
        String nombre_buscar = txtBusqueda.getText();
        // Foreach para que recorra todos los productos
        for (Producto p : Registro.productos) {
            // Verifica que el nombre a buscar este contenido dentro del nombre
            if (p.getNombre().contains(nombre_buscar)) {
                // Condicional para que haga el cálculo si hay productos en 
                // el stock
                if (Integer.parseInt(txtComprar.getText())<= 
                        Integer.parseInt(lblStock.getText())) {
                    
                    // Se guarda el nombre y la cantidad del producto a comprar
                    prodNombres[cont] = p.getNombre() +"                       "
                            +Integer.parseInt(txtComprar.getText());
                    cont++;
                    listProd.setListData(prodNombres);
                    
                    // Se hace el cálculo para ir reduciendo el stock
                    p.setCantidad(p.getCantidad() - 
                            Integer.parseInt(txtComprar.getText()));
                    
                    // Cálcuco del subtotal del producto
                    subtotal += Double.parseDouble(txtComprar.getText()) * precio;
                } else {
                    // Si pasa del límite del stock muestra el siguiente mensaje
                    mensaje = "SOBREPASO EL STOCK EXISTENTE";    
                }
            }
        }
        // Cálculo del iva y el totoal
        iva = 0.12 * subtotal;
        total = iva + subtotal;
        // A mostrar en pantalla el subtotal
        lblSubT.setText(String.valueOf(formato.format(subtotal)));
        // A mostrar el iva en pantalla
        lblIva.setText(String.valueOf(formato.format(iva)));
         // A mostrar el total en pantalla
        lblTotal.setText(String.valueOf(formato.format(total)));
        // A mostrar el mensaje en pantalla por sí se pase del stock
        lbtTot.setText(mensaje);
        
    }//GEN-LAST:event_comprarActionPerformed

